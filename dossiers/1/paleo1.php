  <p>Ce manuscrit italien, exemple célèbre d’écriture <strong>onciale</strong> du type le plus ancien (attesté depuis le IV<sup>e</sup>&nbsp;siècle), présente des formes d’une harmonieuse simplicité, tracées avec fluidité et assurance, au moyen d’un instrument incliné selon un angle constant d’environ 45°. Le fractionnement visible du tracé permet d’analyser à l’œil nu les traits qui composent les lettres. </p>
  <p>La morphologie illustre les <strong>limites du classement théorique</strong> des écritures en majuscules et minuscules ou écritures «&nbsp;bilinéaires&nbsp;» et «&nbsp;quadrilinéaires&nbsp;», selon le nombre des lignes rectrices qui encadrent le corps des lettres et leurs éventuels prolongements supérieurs (hastes) et inférieurs (hampes). L’onciale est habituellement classée parmi les majuscules&nbsp;: outre qu’elle emprunte certaines formes directement à la capitale (surtout B et R), les hastes et hampes y sont souvent à peine esquissées, du moins avant le VI<sup>e</sup>&nbsp;siècle. Ici, les hastes de <em>d</em>, <em>h</em>, <em>l</em> et les hampes de <em>f</em>, <em>p</em>, <em>q</em> (et R dans une moindre mesure) sont au contraires fortement marquées, multipliant environ par deux la hauteur des lettres. </p>
		
